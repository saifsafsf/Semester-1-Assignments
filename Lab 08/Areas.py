def area_tri(base, height):
    '''area_tri(base, height)
        Returns Area of corresponding triangle'''
    tri_area = (base * height)/2    # Using formula
    return tri_area
def area_cir(radius):
    '''area_cir(radius)
        Returns Area of corresponding circle'''     # Documentation
    cir_area = 3.1416 * (radius**2)     # Using formula
    return cir_area
def area_rec(length, width):
    '''area_rec(length, width)
        Returns Area of corresponding rectangle'''
    rec_area = length * width     # Using formula
    return rec_area
